Jak wygląda wizyta kontrolna?

Podczas wizyty kontrolnej dentysta oceni aktualny stan zdrowia jamy ustnej, wszelkie ryzyko wystąpienia choroby w przyszłości oraz udzieli informacji na temat opieki i leczenia koniecznego do zapewnienia dobrego zdrowia jamy ustnej. Ważne jest, abyś sam pomiędzy wizytami starał się zachować zęby w zdrowiu i czystości, aby utrzymać jak najlepszy stan zdrowia jamy ustnej.

Co się stanie podczas kontroli u stamatologa

Podczas wizyty kontrolnej dentysta może podejmować różne działania np. przeprowadzić pełne badanie jamy ustnej, zębów i dziąseł, zapytać o ogólny stan zdrowia i wszelkie problemy, które miałeś z zębami, ustami lub dziąsłami od ostatniej wizyty. Może też zapytać o Twoje przyzwyczajenia związane z czyszczeniem zębów i przekaże porady na temat najodpowiedniejszych sposobów utrzymania zdrowego stanu jamy ustnej, zębów i dziąseł. Nierzadko też postara się wyjaśnić wszelkie ryzyko, a także koszty stomatologiczne, wszelkiego leczenia, jakiego możesz potrzebować.
Wielu z nas przyzwyczaiło się do wizyty u dentysty co sześć miesięcy, ale możesz potrzebować jej częściej lub rzadziej w zależności od tego, jak zdrowe są Twoje usta i zęby. Twój dentysta powinien porozmawiać z Tobą o tym, kiedy powinieneś przyjść na następną wizytę.

Jeśli masz problemy z zębami między kontrolami, skontaktuj się z przychodnią dentystyczną, aby umówić się na wcześniejszą wizytę. Jeśli Twój dentysta zaleci leczenie stomatologiczne - otrzymasz osobisty plan. Obejmuje on wszystkie zabiegi, które masz w ramach NFZ i to ile kosztują te nierefundowane. Jeśli nie otrzymasz planu leczenia, poproś o taki.
Jeśli Twój dentysta mówi, że potrzebujesz określonego rodzaju leczenia, nie powinieneś być proszony o to, by opłacać go prywatnie – chyba, że chcesz skorzystać z alternatywnych opcji.
